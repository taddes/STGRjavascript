// Shorthand tool for controling if/else clauses

let day = 5;

switch (day) {
  case day === 1:
    console.log('Monday')
  case day === 2:
    console.log('Tuesday')
  case day === 3:
    console.log('Wednesday')
  case day === 4:
    console.log('Thursday')
  case day === 5:
    console.log('Friday');
  default:
    console.log('Nothing');
}